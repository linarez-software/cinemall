            $t = ' mil'; 
         }elseif ($num > 1){ 
            $t .= ' mil'; 
         } 
      }elseif ($num == 1) { 
         $t .= ' ' . $matsub[$sub] . '?n'; 
      }elseif ($num > 1){ 
         $t .= ' ' . $matsub[$sub] . 'ones'; 
      }   
      if ($num == '000') $mils ++; 
      elseif ($mils != 0) { 
         if (isset($matmil[$sub])) $t .= ' ' . $matmil[$sub]; 
         $mils = 0; 
      } 
      $neutro = true; 
      $tex = $t . $tex; 
   } 
   $tex = $neg . substr($tex, 1) . $fin; 
   return ucfirst(strtoupper($tex)); 
} 

function datediff($fecha1)
{

		$fecha1=explode("/",$fecha1);
		$base_day		= $fecha1[0];		// no leading "0"
		$base_mon		=  $fecha1[1];		// no leading "0"
		$base_yr		=  $fecha1[2];		// use 4 digit years!
		
		$current_day		= date ("j");
		$current_mon		= date ("n");
		$current_yr		= date ("Y");
		
		
		$base_mon_max		= date ("t",mktime (0,0,0,$base_mon,$base_day,$base_yr));
		
		$base_day_diff 		= $base_mon_max - $base_day;
		
		$base_mon_diff 		= 12 - $base_mon - 1;
		
		$start_day		= 1;
		$start_mon		= 1;
		$start_yr		= $base_yr + 1;
		
		$day_diff	= ($current_day - $start_day) + 1; 	// add today
		$mon_diff	= ($current_mon - $start_mon) + 1;	// add current month
		$yr_diff	= ($current_yr - $start_yr);
		
		$day_diff	= $day_diff + $base_day_diff;
		$mon_diff	= $mon_diff + $base_mon_diff;
		
		if ($day_diff >= $base_mon_max)
		{
			$day_diff = $day_diff - $base_mon_max;
			$mon_diff = $mon_diff + 1;
		}
		
		if ($mon_diff >= 12)
		{
			$mon_diff = $mon_diff - 12;
			$yr_diff = $yr_diff + 1;
		}
		
		$years = "Años";
		$days = "Días";
		$month = "Meses";
		if ($yr_diff == "1") $years = "Año";
		if ($day_diff == "1") $days = "Día";
		if ($mon_diff == "1") $month = "Mes";
		
		// here we go
		$dif=$yr_diff." ".$years." ";
		$dif=$dif.$mon_diff." ".$month." ";
		$dif=$dif.$day_diff." ".$days;
		return ($dif);
}
